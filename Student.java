public class Student {
    public long studentID;
    public String name;

    public void setStudentID(long studentID) {
        this.studentID = studentID;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getStudentID() {
        return studentID;
    }

    public String getName() {
        return name;
    }
}
